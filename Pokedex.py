class Pokemon:
    '''Classe objet Pokémon qui contient un numéro de Pokédex (nombre), un nom (string),
    un tuple de types (string) et des statistiques (nombres)'''
    
    def __init__(self,DexNum,Name,Types,HP,Atk,Def,SpA,SpD,Spe):
        '''Constructeur de la classe Pokemon : prends en paramètres le numéro de Pokédex (nombre), 
        le nom (string), un tuple de types (string), et les statistiques respectives du Pokémon.'''
        self.__dexnum = DexNum
        self.__name = Name
        self.__types = Types
        self.__iv = {
            "HP" : HP,
            "Atk" : Atk,
            "Def" : Def,
            "SpA" : SpA,
            "SpD" : SpD,
            "Spe" : Spe
            }
    
    def get_pokemon(self):
        '''Affiche une liste détaillée des caractéristiques du Pokémon'''
        if self.__types[1] == None:
            print(
                self.__name, self.__dexnum,
                "\nType :", self.__types[0],
                "\nHP :", self.__iv["HP"],
                "\nAttaque :", self.__iv["Atk"],
                "\nDéfense :", self.__iv["Def"],
                "\nAttaque Spé. :", self.__iv["SpA"],
                "\nDéfense Spé. :", self.__iv["SpD"],
                "\nVitesse :", self.__iv["Spe"]
                )
        else:
            print(
                self.__name, self.__dexnum,
                "\nType :", self.__types[0], "/", self.__types[1],
                "\nHP :", self.__iv["HP"],
                "\nAttaque :", self.__iv["Atk"],
                "\nDéfense :", self.__iv["Def"],
                "\nAttaque Spé. :", self.__iv["SpA"],
                "\nDéfense Spé. :", self.__iv["SpD"],
                "\nVitesse :", self.__iv["Spe"]
                )
    
    def get_name(self):
        '''Renvoie le nom du Pokémon'''
        return self.__name
    
    def get_dexnum(self):
        '''Renvoie le numéro de pokédex du Pokémon'''
        return self.__dexnum
    
    def get_types(self):
        '''Renvoie le tuple de types du Pokémon'''
        return self.__types
    
    def get_iv(self):
        '''Renvoie le dictionnaire des statistiques du Pokémon'''
        return self.__iv

